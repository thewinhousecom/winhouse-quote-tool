export function getModulesByIndustry(industrySlug: IndustrySlug): Module[] {
  return modules.filter(module => 
    module.industryIds.includes(industrySlug)
  );
}

export function getModuleById(id: string): Module | undefined {
  return modules.find(module => module.id === id);
}

export function getModuleBySlug(slug: string): Module | undefined {
  return modules.find(module => module.slug === slug);
}

export function getIndustryBySlug(slug: IndustrySlug): Industry | undefined {
  return industries.find(industry => industry.slug === slug);
}

export function getPopularModules(industrySlug: IndustrySlug): Module[] {
  return getModulesByIndustry(industrySlug).filter(module => module.isPopular);
}

export function getRequiredModules(industrySlug: IndustrySlug): Module[] {
  return getModulesByIndustry(industrySlug).filter(module => module.isRequired);
}

export function getModulesByCategory(industrySlug: IndustrySlug, category: string): Module[] {
  return getModulesByIndustry(industrySlug).filter(module => module.category === category);
}

export function calculateDiscount(moduleCount: number, subtotal: number): { percent: number; amount: number } {
  // Discount tiers based on number of modules
  let percent = 0;
  
  if (moduleCount >= 10) {
    percent = 15;
  } else if (moduleCount >= 7) {
    percent = 10;
  } else if (moduleCount >= 5) {
    percent = 5;
  }
  
  return {
    percent,
    amount: (subtotal * percent) / 100,
  };
}

